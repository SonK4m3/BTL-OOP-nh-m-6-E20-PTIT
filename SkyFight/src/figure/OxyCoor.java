package figure;

public class OxyCoor {
	private int x;
	private int y;
	
	public OxyCoor() {
		
	}
	
	public OxyCoor(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public void setCoor(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return this.x;
	}
	
	public int getY() {
		return this.y;
	}
}
